def _detect_risk(text: str) -> str:
    lower = text.lower()
    for keyword in _RISK_KEYWORDS["high"]:
        if keyword in lower:
            return "high"
    for keyword in _RISK_KEYWORDS["medium"]:
        if keyword in lower:
            return "medium"
    return "low"


async def audit_product_card(
    product_id: str,
    product_name: str,
    product_category: str | None,
    db: AsyncSession,
) -> list[LegalCase]:
    await db.execute(
        delete(LegalCase).where(
            LegalCase.product_id == product_id,
            LegalCase.case_type == "card_audit",
        )
    )

    findings = random.sample(_AUDIT_POOL, k=random.randint(2, 3))
    cases: list[LegalCase] = []
    for f in findings:
        case = LegalCase(
            id=str(uuid.uuid4()),
            product_id=product_id,
            case_type="card_audit",
            status="open",
            title=f["title"],
            description=f["description"],
            risk_level=f["risk_level"],
            ai_recommendation=f["ai_recommendation"],
            created_at=datetime.utcnow(),
            updated_at=datetime.utcnow(),
        )
        db.add(case)
        cases.append(case)

    await db.commit()
    for c in cases:
        await db.refresh(c)
    return cases


async def analyze_review_legal(
    product_id: str,
    review_text: str,
    db: AsyncSession,
) -> LegalCase:
    risk = _detect_risk(review_text)
    tmpl = _REVIEW_TEMPLATES[risk]

    snippet = review_text[:120].strip()
    if len(review_text) > 120:
        snippet += "…"

    case = LegalCase(
        id=str(uuid.uuid4()),
        product_id=product_id,
        case_type="review_response",
        status="open",
        title=tmpl["title"],
        description=f"{tmpl['description']}\n\nТекст отзыва: «{snippet}»",
        risk_level=risk,
        ai_recommendation=tmpl["ai_recommendation"],
        created_at=datetime.utcnow(),
        updated_at=datetime.utcnow(),
    )
    db.add(case)
    await db.commit()
    await db.refresh(case)
    return case
